async function seed() {
    console.log('Seeding test articles (direct PostgreSQL)...\n');
    for (const article of ARTICLES) {
        console.log(`  ${article.locale.title}`);
        const { rows } = await pool.query('SELECT id FROM articles WHERE slug = $1 LIMIT 1', [article.slug]);
        let articleId;
        if (rows.length) {
            articleId = rows[0].id;
            await pool.query(
                'UPDATE articles SET is_published = TRUE, cover_image = $1 WHERE id = $2',
                [article.cover_image, articleId]
            );
            console.log(`   Article exists (id=${articleId}), updated publish/cover`);
        } else {
            const result = await pool.query(
                'INSERT INTO articles (slug, is_published, cover_image) VALUES ($1,$2,$3) RETURNING id',
                [article.slug, true, article.cover_image]
            );
            articleId = result.rows[0].id;
            console.log(`   Inserted article id=${articleId}`);
        }
        const loc = article.locale;
        await pool.query(
            `INSERT INTO article_locales (article_id, lang, title, excerpt, html, gallery_json, stack_json)
             VALUES ($1,$2,$3,$4,$5,$6,$7)
             ON CONFLICT (article_id, lang) DO UPDATE SET title=EXCLUDED.title, excerpt=EXCLUDED.excerpt, html=EXCLUDED.html, gallery_json=EXCLUDED.gallery_json, stack_json=EXCLUDED.stack_json`,
            [articleId, loc.lang, loc.title, loc.excerpt, loc.html, '[]', '[]']
        );
        console.log(`   Locale ${loc.lang}`);
    }
    console.log('\nDone.');
    await pool.end();
}

seed().catch(err => {
    console.error('\nSeed failed:', err.message);
    console.error(err);
    process.exit(1);
});
